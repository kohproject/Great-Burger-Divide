  const $ =(id) => document.getElementById(id);


  const findElArr = (str) => {
    let findset = [];
    let displayset = [];
    rests.forEach((itm) => {
      itm.list.forEach((jtm) => {
        if (str.length > 2) {
          if (jtm.name.toLowerCase().indexOf(str) != -1) {
            findset.push(jtm);
          }
        }
      });
    });

    displayset = findset.filter((el) => {
      if (el.name.indexOf(str) != -1) {
        return el;
      }
    });

    buildSelectionDrop(displayset);
  };

  const $drop = $("list_search-drop");
  const removeSelectionDrop = () => {
    if ($drop) {
      Array.from($drop.getElementsByTagName("li")).forEach((el) => {
        el.removeEventListener("click", setMapSelect, false);
        el.removeEventListener("mouseover", setMapSelect, false);
        el.removeEventListener("mouseout", setMapSelect, false);
      });
      $drop.innerHTML = "";
    }
    return true;
  };

  const setMapSelect = (evtType, obj) => {
    var $color = evtType === "mouseout" ? "#fffdf4" : "#dae4fd";
    if (evtType === "click") {
      return;
    }

    if (evtType === "mouseover") {
      var $states = document.getElementsByClassName("state");

      Array.from($states).forEach(state, () => {
        document
          .getElementById("#path_" + state.toUpperCase())
          .style("fill", $color);
      });
      return;
    }

    alert("mm");
    return;
  };

  const buildSelectionDrop = (arr) => {
    if (arr.length === 0) {
      removeSelectionDrop();
      return false;
    }
    if (removeSelectionDrop()) {
      arr.forEach((jel) => {
        let $li = document.createElement("li");
        $li.setAttribute("data-loc", jel.loc);

        $li.innerText = jel.name;
        $li.addEventListener("click", function (e) {
          e.preventDefault();
          setMapSelect(e.type, jel);
          return false;
        });
        $li.addEventListener("mouseover", function (e) {
          setMapSelect(e.type, jel);
        });
        $li.addEventListener("mouseout", function (e) {
          setMapSelect(e.type, null);
        });
        $drop.append($li);
      });
    }
  };


  $("inp_search").addEventListener("keyup", function (evt) {
    var $val = evt.target.value;
    findElArr($val);
  });

  $("wrap_search").addEventListener("mouseenter", function (evt) {
    $("wrap_search").classList.add('focus');
    $("inp_search").focus();
    $("inp_search").value ='';
  });
  $("inp_search").addEventListener("blur", function (evt) {
    $("wrap_search").classList.remove('focus');
  });


